
public class GMSM4 {
	public native byte [] SM4_Encrypt(byte[] in ,byte [] key,byte [] iv,int flag);
	public native byte [] SM4_Decrypt(byte[] in,byte [] key,byte [] iv,int flag);


	public static void main(String[] args) {

		final GMSM4 gmsm4 = new GMSM4();

		final byte [] key = {
			0x01,0x02,0x03,0x04,0x06,0x06,0x07,0x08,
			0x09,0x10,0x11,0x12,0x13,0x14,0x15,0x16
		};

		final byte [] data = {
			0x01,0x02,0x03,0x04,0x05,0x06,0x07,0x08,
			0x09,0x10,0x11,0x12,0x13,0x14,0x15,0x16,
			0x01,0x02,0x03,0x04,0x05,0x06,0x07,0x08,
			0x09,0x10,0x11,0x12,0x13,0x14,0x15,0x16
		};

		final byte [] ciphertext = gmsm4.SM4_Encrypt(data,key,key,0);

	}

	static {
		System.loadLibrary("gmsm4");
	}

}
